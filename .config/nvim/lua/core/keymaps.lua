-- Leaderキー設定
vim.g.mapleader = " "
vim.g.maplocalleader = " "

-- --------------------------------------------------------------------------
-- ウィンドウ操作
-- --------------------------------------------------------------------------
vim.keymap.set("n", "<leader>v", "<C-w>v", { desc = "縦分割" })
vim.keymap.set("n", "<leader>h", "<C-w>s", { desc = "横分割" })
